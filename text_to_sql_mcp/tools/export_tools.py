async def download_export(request: Request) -> Response:
    """GET /exports/{file_id} - serve a previously exported CSV/JSON file."""
    file_id = request.path_params["file_id"]
    if not _SAFE_FILE_ID_RE.match(file_id):
        return JSONResponse({"error": "Invalid file id"}, status_code=400)

    file_path = EXPORT_DIR / file_id
    if not file_path.is_file():
        return JSONResponse({"error": "File not found or expired"}, status_code=404)

    media_type = "text/csv" if file_path.suffix == ".csv" else "application/json"
    return FileResponse(path=file_path, media_type=media_type, filename=file_path.name)
